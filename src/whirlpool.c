static uint64_t* shift_columns(uint64_t* plain)
{
    uint64_t* ret = (uint64_t*)malloc(sizeof(uint64_t) * 8);
    uint64_t k = 0xffUL << 56;

    for (size_t i = 0; i < 8; i++)
        ret[i] = 0;
    
    for (int i = 0; i < 8; i++)
    {
        ret[0] |= plain[(0x8 - i) % 8] & (k >> (i*8));
        ret[1] |= plain[(0x9 - i) % 8] & (k >> (i*8));
        ret[2] |= plain[(0xa - i) % 8] & (k >> (i*8));
        ret[3] |= plain[(0xb - i) % 8] & (k >> (i*8));
        ret[4] |= plain[(0xc - i) % 8] & (k >> (i*8));
        ret[5] |= plain[(0xd - i) % 8] & (k >> (i*8));
        ret[6] |= plain[(0xe - i) % 8] & (k >> (i*8));
        ret[7] |= plain[(0xf - i) % 8] & (k >> (i*8));
    }
    return(ret);
}

static uint8_t GMul(uint8_t a, uint8_t b)
{
    uint8_t p;
    bool lmbit;

    p = 0;
    for (int i = 0; i < 8; i++)
    {
        if(b & 1)
            p ^= a;

        lmbit = a & 0x80;
        a <<= 1;
        if(lmbit)
            a ^= 0x11D;
        b >>= 1;
    }
    return(p);
}

static uint64_t* mix_rows(uint64_t* m_in)
{
    uint64_t C[8] =
    {
        0x0101040108050209,
        0x0901010401080502,
        0x0209010104010805,
        0x0502090101040108,
        0x0805020901010401,
        0x0108050209010104,
        0x0401080502090101,
        0x0104010805020901,
    };
    
    uint64_t* ret = (uint64_t*)malloc(sizeof(uint64_t) * 8);

    for (size_t i = 0; i < 8; i++)
    {
        ret[i] = 0;
        for (int j = 7; j >= 0; j--)
        {
            ret[i] <<= 8;
            ret[i] |= GMul(m_in[i] >> 56, C[0] >> (j * 8)) ^ GMul(m_in[i] >> 48, C[1] >> (j * 8)) ^ GMul(m_in[i] >> 40, C[2] >> (j * 8))  ^ GMul(m_in[i] >> 32, C[3] >> (j * 8))\
                    ^ GMul(m_in[i] >> 24, C[4] >> (j * 8)) ^ GMul(m_in[i] >> 16, C[5] >> (j * 8)) ^ GMul(m_in[i] >> 8, C[6] >> (j * 8))   ^ GMul(m_in[i], C[7] >> (j * 8));
        }
    }
    return(ret);
}

static uint8_t* digest(uint64_t* plain, uint64_t* buffer, size_t n_chunks)
{
    uint64_t RC[10][8] = 
    {
        {0x1823C6E887B8014F,0x0,0x0,0x0,0x0,0x0,0x0,0x0},
        {0x36A6D2F5796F9152,0x0,0x0,0x0,0x0,0x0,0x0,0x0},
        {0x60BC9B8EA30C7B35,0x0,0x0,0x0,0x0,0x0,0x0,0x0},
        {0x1DE0D7C22E4BFE57,0x0,0x0,0x0,0x0,0x0,0x0,0x0},
        {0x157737E59FF04ADA,0x0,0x0,0x0,0x0,0x0,0x0,0x0},
        {0x58C9290AB1A06B85,0x0,0x0,0x0,0x0,0x0,0x0,0x0},
        {0xBD5D10F4CB3E0567,0x0,0x0,0x0,0x0,0x0,0x0,0x0},
        {0xE427418BA77D95D8,0x0,0x0,0x0,0x0,0x0,0x0,0x0},
        {0xFBEE7C66DD17479E,0x0,0x0,0x0,0x0,0x0,0x0,0x0},
        {0xCA2DBF07AD5A8333,0x0,0x0,0x0,0x0,0x0,0x0,0x0},
    };

    while (n_chunks)
    {
        for (int i = 0; i < 8; i++)
            plain[i] = ft_bswap64(plain[i]);
        uint64_t* h = buffer;
        uint64_t* p = add_key(plain, h);
        for (size_t r = 0; r < 10; r++)
        {
            uint64_t* c0 = substitute_bytes(p);
            uint64_t* k0 = substitute_bytes(h);

            uint64_t* c1 = shift_columns(c0);
            uint64_t* k1 = shift_columns(k0);
            free(c0);
            free(k0);

            uint64_t* c2 = mix_rows(c1);
            uint64_t* k2 = mix_rows(k1);
            free(c1);
            free(k1);
            free(p);
            if(h != buffer)
                free(h);
            
            h = add_key(k2, RC[r]);
            p = add_key(c2, h);
            free(c2);
            free(k2);
        }
        for (size_t i = 0; i < 8; i++)
            buffer[i] ^= plain[i] ^ p[i];
        free(h);
        free(p);
        plain += 8;
        n_chunks--;
    }
    for (int i = 0; i < 8; i++)
        buffer[i] = ft_bswap64(buffer[i]);
    
    int hash_len = 512/8;
    uint8_t* hash = (uint8_t*)malloc(hash_len + 1);
    for(int i = 0; i < hash_len; i++)
        hash[i] = *((uint8_t*)buffer + i);
    hash[hash_len] = '\0';
    return (hash);
}

uint8_t* whirlpool(uint8_t* argv, uint64_t len)
{
    uint64_t* padded_text;
    uint8_t* hash;
    
    padded_text = add_padding(argv, &len, 64, 32);

    if(!padded_text)
        return(NULL);
    bswap_length_field(padded_text + ((len - 32) / 8), 32);

    uint64_t* H_0 = (uint64_t*)malloc(sizeof(uint64_t) * 8);
    for (size_t i = 0; i < 8; i++)
        H_0[i] = 0x0;

    hash = digest(padded_text, H_0, (len * 8) / 512);

    free(H_0);
    free(padded_text);
    return(hash);
}
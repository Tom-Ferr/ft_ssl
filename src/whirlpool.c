static uint64_t* add_key(uint64_t* plain, uint64_t* key)
{
    uint64_t* ret = (uint64_t*)malloc(sizeof(uint64_t) * 8);

    for (size_t i = 0; i < 8; i++)
        ret[i] = plain[i] ^ key[i];
    return(ret);
}

